'use client';

import { useState, useMemo } from 'react';
import { UpdateItem, Priority } from '@/lib/types';
import { copyToClipboard } from '@/lib/docx-generator';
import { 
  Share2, 
  Copy, 
  Check, 
  Calendar,
  ChevronDown,
  ChevronUp,
  AlertCircle,
  Zap,
  MessageSquare,
} from 'lucide-react';

interface ShareUpdateProps {
  updates: UpdateItem[];
}

type ShareFormat = 'slack' | 'email' | 'brief';

const FORMAT_OPTIONS: { id: ShareFormat; label: string; description: string }[] = [
  { id: 'slack', label: 'Slack Message', description: 'Formatted for Slack with emoji' },
  { id: 'email', label: 'Email Update', description: 'Professional email format' },
  { id: 'brief', label: 'Brief Summary', description: 'Quick bullet points only' },
];

function formatTime(minutes: number | null): string {
  if (minutes === null) return '';
  const hours = Math.floor(minutes / 60);
  const mins = minutes % 60;
  const period = hours >= 12 ? 'PM' : 'AM';
  const displayHours = hours > 12 ? hours - 12 : hours === 0 ? 12 : hours;
  return `${displayHours}:${mins.toString().padStart(2, '0')} ${period}`;
}

